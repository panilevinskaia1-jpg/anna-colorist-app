// ─── Расписание ───────────────────────────────────────────────
// Рабочие слоты по дням недели (0=Вс, 1=Пн, ..., 6=Сб)
// [] означает выходной день
const WORK_SCHEDULE = {
  0: [],                                                          // Воскресенье — выходной
  1: ['10:00','10:30','12:00','12:30','14:00','16:00','16:30'],   // Понедельник
  2: ['10:00','11:00','11:30','13:00','15:00','15:30','17:00'],   // Вторник
  3: [],                                                          // Среда — выходной
  4: ['10:00','10:30','12:00','14:00','14:30','16:00','18:00'],   // Четверг
  5: ['11:00','11:30','13:00','13:30','15:00','17:00'],           // Пятница
  6: ['10:00','10:30','11:00','12:00','13:00','14:00','15:00'],   // Суббота
};

/**
 * Возвращает доступные слоты на конкретную дату.
 * В реальном проекте здесь должен быть запрос к API.
 * Сейчас: детерминированно убираем часть слотов для демо.
 */
function getAvailableSlots(dateStr) {
  const date = new Date(dateStr + 'T00:00:00');
  const dayOfWeek = date.getDay();
  const baseSlots = WORK_SCHEDULE[dayOfWeek] || [];
  if (baseSlots.length === 0) return [];

  // Детерминированная "занятость" по дате (без Math.random)
  const dayNum = date.getDate();
  return baseSlots.filter((_, idx) => (idx + dayNum) % 3 !== 0);
}

/**
 * Возвращает массив строк 'YYYY-MM-DD' — доступные даты
 * для выбора в календаре (следующие 45 дней от сегодня).
 */
function getAvailableDates() {
  const dates = [];
  const today = new Date();
  for (let i = 1; i <= 45; i++) {
    const d = new Date(today);
    d.setDate(today.getDate() + i);
    const dow = d.getDay();
    if ((WORK_SCHEDULE[dow] || []).length > 0) {
      dates.push(d.toISOString().split('T')[0]);
    }
  }
  return dates;
}

